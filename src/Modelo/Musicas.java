package Modelo;


import java.util.Calendar;


public class Musicas {
    protected String idMusica;
    protected String duracao;
    protected String nomeMusica;
    protected String lancamento;
    protected String faixa;
    protected Calendar dataDeCadastro;
    protected String idFornecedor;
    protected String idGenero;
    protected String idAlbum;
    protected String arquivo;
    protected String idCantor;
    protected String idCompositor;
    protected String gravadora;

    public String getIdMusica() {
        return idMusica;
    }

    public void setIdMusica(String idMusica) {
        this.idMusica = idMusica;
    }

    public String getDuracao() {
        return duracao;
    }

    public void setDuracao(String duracao) {
        this.duracao = duracao;
    }

    public String getNomeMusica() {
        return nomeMusica;
    }

    public void setNomeMusica(String nomeMusica) {
        this.nomeMusica = nomeMusica;
    }

    public String getLancamento() {
        return lancamento;
    }

    public void setLancamento(String lancamento) {
        this.lancamento = lancamento;
    }

    public String getFaixa() {
        return faixa;
    }

    public void setFaixa(String faixa) {
        this.faixa = faixa;
    }

    public Calendar getDataDeCadastro() {
        return dataDeCadastro;
    }

    public void setDataDeCadastro(Calendar dataDeCadastro) {
        this.dataDeCadastro = dataDeCadastro;
    }

    public String getIdFornecedor() {
        return idFornecedor;
    }

    public void setIdFornecedor(String idFornecedor) {
        this.idFornecedor = idFornecedor;
    }

    public String getIdGenero() {
        return idGenero;
    }

    public void setIdGenero(String idGenero) {
        this.idGenero = idGenero;
    }

    public String getIdAlbum() {
        return idAlbum;
    }

    public void setIdAlbum(String idAlbum) {
        this.idAlbum = idAlbum;
    }

    public String getArquivo() {
        return arquivo;
    }

    public void setArquivo(String arquivo) {
        this.arquivo = arquivo;
    }

    public String getIdCantor() {
        return idCantor;
    }

    public void setIdCantor(String idCantor) {
        this.idCantor = idCantor;
    }

    public String getIdCompositor() {
        return idCompositor;
    }

    public void setIdCompositor(String idCompositor) {
        this.idCompositor = idCompositor;
    }

    public String getGravadora() {
        return gravadora;
    }

    public void setGravadora(String gravadora) {
        this.gravadora = gravadora;
    }

    
   
  
}
