    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jtMusica.getModel();
        
        AcessoMusica accMus = new AcessoMusica();
        
        for(Musicas mus: accMus.read()){
            modelo.addRow(new Object[]{
                mus.getIdMusica(),
                mus.getNomeMusica(),
                mus.getIdCantor()
            });    

        }
    }

    
    
    
